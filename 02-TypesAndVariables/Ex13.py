#####
# Calculation of the area and circumference of a circle
##

# determine radius and PI
r = 5
pi = 3.14 # I think there is somewhere method to get value of pi as a constant

# calculate area 
area = pi * r ** 2

# calculate circumference 
circ = 2 * pi * r

# display results 
msg = f"radius: {r}, area: {area}, circumference: {circ}"
print(msg)
